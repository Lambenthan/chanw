"""阳明 5 个人格维度的文本标记词表

设计原则:
  - 用规则化字符串匹配, 而不是 LLM API ——可复现、零成本、可验证
  - 每个标记都基于古文中明确的语用功能, 不是随便选词
  - 每个维度都有正向标记和负向标记, 分数 = 正向密度 - 负向密度
  - 全部归一化为"每千字密度", 消除条目长度影响

后续如果概念验证通过, 再用 LLM API 做精细打分对照.
"""

# ============================================================================
# 维度 1: 教学耐心 (Teaching Patience)
#   测的是阳明对学生疑问的回应温度
# ============================================================================
PATIENT_MARKERS = [
    # 缓和 / 鼓励 / 接纳
    "且", "甚是", "善", "善哉", "诚然", "如此",
    "尔等", "诸君", "汝辈",
    "亦未为不可", "亦犹", "也好",
    "试言之", "可", "可矣",
    # 类比 / 举例引导
    "譬之", "譬如", "比如",
    "且如", "正如",
]

IMPATIENT_MARKERS = [
    # 责备 / 强否定
    "此说之蔽久矣", "差矣", "误矣", "缪矣",
    "蔽于", "误于", "失却",
    # 反诘
    "岂可", "宁能", "焉能",
    # 训斥
    "汝徒", "徒为", "徒事", "尔何",
]


# ============================================================================
# 维度 2: 反权威程度 (Anti-Authority)
#   测的是阳明对朱子/先儒/世儒/经典权威的语气强度
#   越高 = 越直接挑战权威; 越低 = 越谦卑深恭维或回避
# ============================================================================
ANTI_AUTH_MARKERS = [
    # 直接否定权威
    "非也", "非是", "不然",
    "差矣", "缪矣", "误矣",
    "未尽", "未明", "未当", "未然",
    "失却", "失之",
    # 批评先贤说法
    "此说差", "此说蔽", "此说误",
    "蔽于支离", "支离", "锢蔽",
    "二氏之学", "外驰",
    # 自信主张
    "吾以为", "某以为",
]

DEFERENT_MARKERS = [
    # 自我贬低
    "愚以为", "鄙人", "区区",
    "未敢", "不敢", "岂敢",
    "某诚", "某直", "某窃",
    # 不确定
    "或然", "或者", "未知是否",
    "未审", "幸示",
    # 退让
    "亦未尝不可", "亦可备一说",
]


# ============================================================================
# 维度 3: 自我修正频率 (Self-Correction)
#   测的是阳明承认自己之前说错 / 重新思考的标记
# ============================================================================
SELF_CORRECT_MARKERS = [
    "前以", "前所", "前曾", "前说",
    "始信", "始知",
    "今乃", "今则", "今复", "今益",
    "向之", "向以", "向曾",
    "尝以", "尝谓",
    "近觉", "近始",
    "更思之", "更详之",
    "未尽", "未明",
]

# 这个维度没有"反向标记", 因为不承认错误是默认状态——只数正向出现


# ============================================================================
# 维度 4: 同理心 (Empathy)
#   测的是阳明对学生处境的体察 + 用第二人称(你)的方式
# ============================================================================
EMPATHY_MARKERS = [
    # 共情体察
    "知汝", "知尔", "知君", "可知",
    "汝忧", "汝困", "汝疑", "汝心",
    "尔心", "尔意", "君心",
    # 体察对方处境
    "亦犹", "若以", "若使",
    "在君", "在汝", "在尔",
    # 鼓励性指代
    "诸君", "诸友", "诸生",
    # 反求诸己的引导
    "试思", "试自思", "试反观",
]

# 反向: 冷漠 / 距离感 (古文里这种标记不多, 用一些)
DISTANT_MARKERS = [
    "尔何", "汝徒", "徒为",
]


# ============================================================================
# 维度 5: 实践导向 (Practice Orientation)
#   测的是阳明强调"上手做"而不是"想清楚再做"的程度
# ============================================================================
PRACTICE_MARKERS = [
    "用功", "用力",
    "工夫", "下手",
    "实用", "切实", "着实",
    "事上", "事上磨", "事上用功",
    "实地", "实地用功",
    "亲历", "身亲", "履历",
    "在事上", "就事上",
]

# 反向: 空谈 / 纯思辨 (阳明很少这样写, 所以这个反向很弱)
ABSTRACT_MARKERS = [
    "空言", "徒言", "徒思",
    "悬想", "悬空",
]


# ============================================================================
# 汇总: 5 个维度的正向 / 反向标记
# ============================================================================
DIMENSIONS = {
    "教学耐心": (PATIENT_MARKERS,    IMPATIENT_MARKERS),
    "反权威":   (ANTI_AUTH_MARKERS,  DEFERENT_MARKERS),
    "自我修正": (SELF_CORRECT_MARKERS, []),
    "同理心":   (EMPATHY_MARKERS,    DISTANT_MARKERS),
    "实践导向": (PRACTICE_MARKERS,   ABSTRACT_MARKERS),
}


if __name__ == "__main__":
    print("=== 五个人格维度标记词统计 ===")
    for dim, (pos, neg) in DIMENSIONS.items():
        print(f"\n[{dim}]")
        print(f"  正向标记 ({len(pos)} 个): {', '.join(pos[:10])}{'...' if len(pos) > 10 else ''}")
        print(f"  反向标记 ({len(neg)} 个): {', '.join(neg) if neg else '(无)'}")
